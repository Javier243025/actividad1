class CentrosComerciales:
    
    def __init__(self, nombre, direccion, numero_tiendas, capacidad_parqueo):
        self.nombre = nombre
        self.direccion = direccion
        self.numero_tiendas = numero_tiendas
        self.capacidad_parqueo = capacidad_parqueo

def __str__(self):
        return f"{self.nombre} ubicado en {self.direccion} con {self.numero_tiendas} tiendas y capacidad de parqueo para {self.capacidad_parqueo} autos."